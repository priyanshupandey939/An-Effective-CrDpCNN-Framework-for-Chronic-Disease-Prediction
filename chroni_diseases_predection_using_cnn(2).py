import numpy as np
from sklearn.model_selection import StratifiedKFold
from sklearn.metrics import classification_report, confusion_matrix
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Conv1D, MaxPooling1D, Flatten, Dense, Dropout
from tensorflow.keras.utils import to_categorical
import matplotlib.pyplot as plt
import seaborn as sns

# ── Step 1: Data Prepare ──────────────────────────────────────
X = X_pca                          # PCA output (1659, 49)
y = df_clean["Diagnosis"].values   # Target column

le = LabelEncoder()
y_encoded = le.fit_transform(y)
num_classes = len(np.unique(y_encoded))

# ── Step 2: K-Fold Setup ──────────────────────────────────────
n_splits = 5   # 5-Fold (change kar sakte ho 10 bhi)
skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)

# Results store karne ke liye
fold_train_acc = []
fold_test_acc  = []
fold_train_loss = []
fold_test_loss  = []
all_y_true = []
all_y_pred = []

print(f"{'='*45}")
print(f"   Stratified {n_splits}-Fold Cross Validation - CNN")
print(f"{'='*45}\n")

# ── Step 3: K-Fold Loop ───────────────────────────────────────
for fold, (train_idx, test_idx) in enumerate(skf.split(X, y_encoded)):
    print(f"🔁 Fold {fold+1}/{n_splits} training...")

    # Data split
    X_train, X_test = X[train_idx], X[test_idx]
    y_train = to_categorical(y_encoded[train_idx], num_classes)
    y_test  = to_categorical(y_encoded[test_idx],  num_classes)

    # Reshape for CNN
    X_train = X_train.reshape(X_train.shape[0], X_train.shape[1], 1)
    X_test  = X_test.reshape(X_test.shape[0],   X_test.shape[1],  1)

    # ── Model banao (har fold mein naya) ──────────────────────
    model = Sequential([
        Conv1D(32, 2, activation='relu', input_shape=(X_train.shape[1], 1)),
        MaxPooling1D(2),

        Conv1D(64, 2, activation='relu'),
        MaxPooling1D(2),

        Flatten(),

        Dense(64, activation='relu'),
        Dropout(0.5),

        Dense(num_classes, activation='softmax')
    ])

    model.compile(
        optimizer='adam',
        loss='categorical_crossentropy',
        metrics=['accuracy']
    )

    # ── Train ─────────────────────────────────────────────────
    from tensorflow.keras.callbacks import EarlyStopping
    early_stop = EarlyStopping(monitor='val_loss', patience=8,
                               restore_best_weights=True, verbose=0)

    model.fit(
        X_train, y_train,
        epochs=100,
        batch_size=32,
        validation_split=0.1,
        callbacks=[early_stop],
        verbose=0   # 0 = quiet mode, fold results neeche print honge
    )

    # ── Evaluate ──────────────────────────────────────────────
    tr_loss, tr_acc = model.evaluate(X_train, y_train, verbose=0)
    te_loss, te_acc = model.evaluate(X_test,  y_test,  verbose=0)

    fold_train_acc.append(tr_acc)
    fold_test_acc.append(te_acc)
    fold_train_loss.append(tr_loss)
    fold_test_loss.append(te_loss)

    # Predictions store karo
    y_pred = np.argmax(model.predict(X_test, verbose=0), axis=1)
    y_true = np.argmax(y_test, axis=1)
    all_y_true.extend(y_true)
    all_y_pred.extend(y_pred)

    print(f"   ✅ Train Acc: {tr_acc*100:.2f}%  |  Test Acc: {te_acc*100:.2f}%\n")

# ── Step 4: Final Summary ─────────────────────────────────────
print(f"{'='*45}")
print(f"         FINAL K-FOLD RESULTS")
print(f"{'='*45}")
for i in range(n_splits):
    print(f"  Fold {i+1}: Train {fold_train_acc[i]*100:.2f}%  |  Test {fold_test_acc[i]*100:.2f}%")

print(f"\n  Mean Train Accuracy : {np.mean(fold_train_acc)*100:.2f}% ± {np.std(fold_train_acc)*100:.2f}%")
print(f"  Mean Test  Accuracy : {np.mean(fold_test_acc)*100:.2f}%  ± {np.std(fold_test_acc)*100:.2f}%")
print(f"  Mean Train Loss     : {np.mean(fold_train_loss):.4f}")
print(f"  Mean Test  Loss     : {np.mean(fold_test_loss):.4f}")

# Overfit check
diff = np.mean(fold_train_acc) - np.mean(fold_test_acc)
print(f"\n  Difference (Train-Test): {diff*100:.2f}%")
if np.mean(fold_train_acc) < 0.75 and np.mean(fold_test_acc) < 0.75:
    print("  ⚠️  UNDERFIT — Layers ya filters badhao")
elif diff > 0.10:
    print("  ⚠️  OVERFIT  — Dropout badhao ya epochs kam karo")
else:
    print("  ✅  GOOD FIT — Model reliable hai!")
print(f"{'='*45}")

# ── Step 5: Overall Classification Report ────────────────────
print("\n===== Overall Classification Report (All Folds) =====")
print(classification_report(all_y_true, all_y_pred,
                             target_names=['No Disease', 'Disease']))

